// SymbolID returns the fixed ID for a known linguistic symbol, or (0, false)
// if the string is not a symbol.
func SymbolID(name string) (uint64, bool) {
	id, ok := symbolsByStr[name]
	return id, ok
}

// SymbolName returns the string for a fixed symbol ID, or ("", false)
// if the ID is not a symbol.
func SymbolName(id uint64) (string, bool) {
	name, ok := symbolsByID[id]
	return name, ok
}
